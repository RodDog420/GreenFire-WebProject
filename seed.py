def seed():
    app = create_app(DevConfig)
    with app.app_context():
        added   = 0
        skipped = 0

        for item in PRODUCTS:
            if Product.query.filter_by(slug=item['slug']).first():
                print(f'  SKIP  {item["slug"]}')
                skipped += 1
                continue

            variants   = item.get('variants')
            attributes = item.get('attributes')

            product = Product(
                slug                 = item['slug'],
                name                 = item['name'],
                product_type         = item['product_type'],
                subcategory          = _clean(item.get('subcategory')),
                series               = _clean(item.get('series')),
                credit_label         = _clean(item.get('credit_label')),
                credit               = _clean(item.get('credit')),
                instagram            = _handle(item.get('instagram')),
                collab               = _clean(item.get('collab')),
                price_cents          = item['price_cents'],
                height               = _clean(item.get('height')),
                technique            = _clean(item.get('technique')),
                joint_size           = _clean(item.get('joint_size')),
                glass_color          = _clean(item.get('glass_color')),
                glass_color_company  = _clean(item.get('glass_color_company')),
                gemstones            = item.get('gemstones', False),
                electroform          = item.get('electroform', False),
                fume                 = item.get('fume', False),
                description          = item.get('description') or '',
                is_sold              = item.get('is_sold', False),
                is_active            = item.get('is_active', True),
                is_featured          = item.get('is_featured', False),
                featured_order       = item.get('featured_order'),
                quantity             = item.get('quantity', 1),
                meta_description     = _clean(item.get('meta_description')),
                perc                 = _clean(item.get('perc')),
                reclaimer            = item.get('reclaimer', False),
                includes             = _clean(item.get('includes')),
                variants_json        = json.dumps(variants) if variants else None,
                attributes_json      = json.dumps(attributes) if attributes else None,
                is_premium           = item.get('is_premium', False),
                metal_type           = _clean(item.get('metal_type')),
                primary_image        = item.get('primary_image'),
                acquisition_type     = item.get('acquisition_type', 'outright'),
            )
            db.session.add(product)
            db.session.flush()

            for i, img_url in enumerate(item.get('images', [])):
                db.session.add(ProductImage(
                    product_id = product.id,
                    image_url  = img_url,
                    alt_text   = item['name'],
                    sort_order = i,
                ))

            print(f'  ADD   {item["slug"]}')
            added += 1

        db.session.commit()
        print(f'\nDone — {added} added, {skipped} skipped.')


if __name__ == '__main__':
    seed()
