def _clean(val):
    """Return None for null-ish values: None, 'None', empty string."""
    if val is None or val == 'None' or val == '':
        return None
    return val


def _handle(instagram):
    """Normalise Instagram to a bare handle, stripping full URLs."""
    val = _clean(instagram)
    if not val:
        return None
    val = re.sub(r'https?://(www\.)?instagram\.com/', '', val).strip('/')
    return val or None


